function abracadabra() {
	let prenom = prompt("prenom ?");
	let nom = prompt("nom ?");
	let age = prompt("age ?");

	alert(
		"Sapristi ! On ne m'avait pas prévenu que c'était vous, " +
			prenom +
			"! Euh... Je veux dire... Monsieur le grand magicien " +
			nom +
			" ! Cela fait déjà " +
			age +
			" ans que vous faites rayonner notre contrée !"
	);
}
abracadabra();
